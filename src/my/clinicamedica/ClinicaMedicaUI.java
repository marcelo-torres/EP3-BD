    private void btn_eqpMedica_NOVOActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_eqpMedica_NOVOActionPerformed
        //ao clicar em Novo, todos campos devem ser resetados
        limparCamposMedicos();
        // os botÃµes devem ser desbloqueados
        desbloquearBotoes(true);
        desbloquearCampos(true);
    }//GEN-LAST:event_btn_eqpMedica_NOVOActionPerformed

    private void btn_eqpMedica_cancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_eqpMedica_cancelarActionPerformed
        limparCamposMedicos();

        desbloquearBotoes(false);
        desbloquearCampos(false);
    }//GEN-LAST:event_btn_eqpMedica_cancelarActionPerformed

    private void btn_eqpMedica_salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_eqpMedica_salvarActionPerformed
        limparCamposMedicos();

        desbloquearBotoes(false);
        desbloquearCampos(false);

        Especialidade especialidade = new Especialidade(1, c_eqpMedica_especialidade.getText());
        Telefone telefone = new Telefone(c_eqpMedica_telefone.getText());

        medico.setNome(c_eqpMedica_nome.getText());
        medico.setTelefone(telefone);
        medico.setCRM(Integer.valueOf(c_eqpMedica_CRM.getText()));
        medico.adicionarEspecialidade(especialidade);

        //Salvar o mÃ©dico no BD
        MedicoDAOPostgresql dao = new MedicoDAOPostgresql();
        try {
            dao.gravar(medico);
        } catch (BancoDeDadosException ex) {
            Logger.getLogger(ClinicaMedicaUI.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(ClinicaMedicaUI.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btn_eqpMedica_salvarActionPerformed

    //METODOS AUXILIARES
    private void limparCamposMedicos() {
        c_eqpMedica_nome.setText("");
        c_eqpMedica_CRM.setText("");
        c_eqpMedica_telefone.setText("");
        c_eqpMedica_especialidade.setText("");
    }

    private void desbloquearBotoes(boolean condicao) {
        btn_eqpMedica_salvar.setEnabled(condicao);
        btn_eqpMedica_cancelar.setEnabled(condicao);
    }

    private void desbloquearCampos(boolean condicao) {
        c_eqpMedica_nome.setEnabled(condicao);
        c_eqpMedica_telefone.setEnabled(condicao);
        c_eqpMedica_CRM.setEnabled(condicao);
        c_eqpMedica_especialidade.setEnabled(condicao);
    }

